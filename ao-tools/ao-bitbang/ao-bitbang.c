#include "ccdbg.h"

int
main (int argc, char **argv)
{
	struct ccdbg	*dbg;

	dbg = ccdbg_open("BITBANG");
	if (!dbg)
		exit (1);

	ccdbg_add_debug(CC_DEBUG_BITBANG);

	ccdbg_manual(dbg, stdin);
}
